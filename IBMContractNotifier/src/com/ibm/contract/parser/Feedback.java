package com.ibm.contract.parser;

public class Feedback {

	int no_of_feedbacks;
	String project_description;
	String project_name;
	/**
	 * @return the no_of_feedbacks
	 */
	public int getNo_of_feedbacks() {
		return no_of_feedbacks;
	}
	/**
	 * @param no_of_feedbacks the no_of_feedbacks to set
	 */
	public void setNo_of_feedbacks(int no_of_feedbacks) {
		this.no_of_feedbacks = no_of_feedbacks;
	}
	/**
	 * @return the project_description
	 */
	public String getProject_description() {
		return project_description;
	}
	/**
	 * @param project_description the project_description to set
	 */
	public void setProject_description(String project_description) {
		this.project_description = project_description;
	}
	/**
	 * @return the project_name
	 */
	public String getProject_name() {
		return project_name;
	}
	/**
	 * @param project_name the project_name to set
	 */
	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}
	
}
